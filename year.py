# 取得使用者輸入的年份，並轉換為整數
year = int(input("請輸入一個西元年份: "))

# 利用 if, elif, else 的順序性來判斷
# 條件必須由「最嚴格」排到「最寬鬆」

if year % 400 == 0:
    # 如果能被 400 整除，一定是閏年
    print(f"{year} 年是閏年")
    
elif year % 100 == 0:
    # 走到這裡，代表「不能被 400 整除」，但「能被 100 整除」，所以是平年
    print(f"{year} 年是平年")
    
elif year % 4 == 0:
    # 走到這裡，代表「不能被 100 整除」，但「能被 4 整除」，所以是閏年
    print(f"{year} 年是閏年")
    
else:
    # 以上皆非，就是平年
    print(f"{year} 年是平年")