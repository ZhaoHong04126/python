def get_zodiac(year):
    # 生肖列表（從餘數0對應的猴年開始順序排列，餘數為4是鼠年）
    # 這裡的順序為：0:猴, 1:雞, 2:狗, 3:豬, 4:鼠, 5:牛, 6:虎, 7:兔, 8:龍, 9:蛇, 10:馬, 11:羊
    zodiacs = ('猴', '雞', '狗', '豬', '鼠', '牛', '虎', '兔', '龍', '蛇', '馬', '羊')
    # 計算公式: (年份 - 3) % 12 或直接使用 4 (鼠年) 的基數計算
    # 這裡使用 (year - 4) % 12，若 year為 2024: (2024-4)%12 = 0 -> 猴 (不對)，
    # 正確公式為 (year - 4) % 12 得到的索引需重新對應。
    
    # 更簡便的寫法：將猴鼠順序對好，year%12=4是鼠，3是豬...
    # (year - 4) % 12，結果對應 0:鼠, 1:牛... 11:豬
    # 以下使用經典的「(year - 4) % 12」來計算以「鼠」為索引0的方法
    # 或者用更直觀的 index = (year - 1900) % 12，1900是鼠年
    
    # 推薦寫法：
    idx = (year - 4) % 12
    zodiac_order = ('鼠', '牛', '虎', '兔', '龍', '蛇', '馬', '羊', '猴', '雞', '狗', '豬')
    return zodiac_order[idx]

# 測試
year = int(input("請輸入年份: "))
print(f"{year}年是{get_zodiac(year)}年")
